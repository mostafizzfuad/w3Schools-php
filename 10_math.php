<?php

echo(pi());
echo PHP_EOL;

echo(min(0, 150, 30, 20, -8, -200));  // returns -200
echo PHP_EOL;

echo(max(0, 150, 30, 20, -8, -200));  // returns 150
echo PHP_EOL;

echo(abs(-6.7));  // returns 6.7
echo PHP_EOL;

echo(sqrt(64));  // returns 8
echo PHP_EOL;

echo(round(0.60));  // returns 1
echo PHP_EOL;
echo(round(0.49));  // returns 0
echo PHP_EOL;

echo(rand());
echo PHP_EOL;

echo(rand(10, 100));

